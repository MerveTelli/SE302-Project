       String course_languageA="İngilizce";
         if (l_turkish.isSelected()) {
             course_languageA="Türkçe";
            
        }
         else if (l_second.isSelected()) {
             course_languageA="İkinci Yabancı Dil";
         }
             
        String course_typeA = "Zorunlu";
         if(t_elective.isSelected()) {
             course_typeA="Seçmeli";                    
        }
        String course_levelA = "Ön Lisans";
        if (c_first.isSelected()) {
             course_levelA="Lisans";
            
        }
         else if (c_third.isSelected()) {
             course_levelA="Yüksek Lisans";
         }
        else if (c_second.isSelected()) {
             course_levelA="Doktora";
         }
        
        
        String course_categoryA="Temel Ders";
        if (c_major.isSelected()) {
             course_categoryA="Uzmanlık/Alan Dersi";
            
        }
         else if (c_supportive.isSelected()) {
             course_categoryA="Destek Dersi";
         }
        else if (c_communi.isSelected()) {
             course_categoryA="İletişim ve Yönetim Becerileri Dersi";
         }
        else if (c_transferable.isSelected()) {
             course_categoryA="Aktarılabilir Beceri Dersi";
         }
        
        String course_semesterA = "Güz";
        if(j_spring.isSelected()) {
             course_semesterA="Bahar";                    
        }
        
        
        if (kontrol_et()) 
            if (!course_code_kontrolet(course_codeA)) {
            PreparedStatement st;
            ResultSet rs;
            
            String sorgu="INSERT INTO `general_information`(`c_name`, `c_code`, `c_semester`, `c_theory`, `c_application`, `local_credit`, `c_ects`, `c_prerequi`, `c_language`, `c_type`, `c_level`, `c_coordinator`, `c_lecturers`, `c_assistants`, `c_objectives`, `c_lo`, `c_description`, `c_category`, `c_notes`, `c_suggested`) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?) ";
            try {
                st=baglanti.getConnection().prepareStatement(sorgu);
                st.setString(1, course_nameA);
                st.setString(2, course_codeA);
                st.setString(3, course_semesterA);
                st.setString(4, course_theoryA);
                st.setString(5, course_appA);
                st.setString(6, course_localA);
                st.setString(7, course_aktsA);
                st.setString(8, course_prereA);
                st.setString(9, course_languageA);
                st.setString(10, course_typeA);
                st.setString(11, course_levelA);
                st.setString(12, course_coorA);
                st.setString(13, course_lecturerA);
                st.setString(14, course_asisstantA);
                st.setString(15, course_objectivesA);
                st.setString(16, course_losA);
                st.setString(17, course_descrA);
                st.setString(18, course_categoryA);
                st.setString(19, course_notesA);
                st.setString(20, course_suggestedA);
                
                 if (st.executeUpdate()!=0) {
                     JOptionPane.showMessageDialog(null, "Saved");
                     Giris giris= new Giris();
                     setVisible(false);
                     giris.setVisible(true);
                     
                     
                    
                }
                 else{
                     JOptionPane.showMessageDialog(null, "Not Saved");
                 }
                
                
            } catch (Exception e) {
                e.printStackTrace();
            }
           
            
        }
        
       
        
       
