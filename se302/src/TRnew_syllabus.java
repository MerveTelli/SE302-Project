       boolean course_code_kontrolet(String course_code){
        boolean course_code_varligi = false;
        PreparedStatement st;
        ResultSet rs;
        String sorgu= "SELECT * FROM general_information WHERE c_code= ?";
           try {
               st=baglanti.getConnection().prepareStatement(sorgu);
               st.setString(1, course_code);
               rs=st.executeQuery();
                 if (rs.next()) {
                     
                     course_code_varligi=true;
                     JOptionPane.showMessageDialog(null, "This Course Saved", "Warning", 2);
                     this.jt_code.setText("");
               }
           } catch (Exception e) {
               e.printStackTrace();
               
           }
           return course_code_varligi;
    }
        
    
       
        
        
    

    
    
    
